			function createDiv(index) {
				var div = document.createElement("div");
			  
				div.style.width = "180px";
				div.style.height = "250px";
				div.style.border = "medium solid black";
				div.style.color = "black";
				div.style.fontSize = "25px";
				div.style.display = "inline-grid";
				div.style.padding = "10px";
				div.style.margin = "10px";
				div.style.textAlign = "center";
				
				var name = document.createElement("h1");
				name.innerText = ninja_list[index].name + ".";
				
				
				var image = document.createElement("img");				
				image.src = "./SpriteImages/" + ninja_list[index].name + "/Capture.PNG";
				image.style.marginRight = "auto";
				image.style.marginLeft = "auto";
				
				
				//Set image size based on dimensions
				if (ninja_list[index].width > ninja_list[index].height) {
					image.setAttribute("width", div.style.width);
				}
				else {
					image.setAttribute("height", "150px");
				}
				var dimensions = document.createElement("h2");
				if (ninja_list[index].width > 0) {
					dimensions.innerText = ninja_list[index].width + " x " + ninja_list[index].height;
				}
				
				
				var button = document.createElement("button");
				button.innerText = "Copy";
				
				if (ninja_list[index].width === 0) {
					button.disabled = true;
				}
			
				
				
				button.addEventListener("click", (event) => {
					navigator.clipboard.writeText(ninja_list[index].sprite);
				});		
			
				div.appendChild(document.createTextNode(name.innerText));
				div.appendChild(image);
				div.appendChild(document.createTextNode(dimensions.innerText));
				div.appendChild(button);
				
				return div;
			}
			
